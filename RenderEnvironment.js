function groundBox()
{
  var ground = new Cube();
  if (showNormal)
  {
    ground.textureNum = -3;
  }
  else
  {
    ground.textureNum = 1
  }
  ground.specularOn = 0;
  ground.color = [1.0,0.2,0.3];
  ground.matrix.translate(0.0, -1, 0.2);
  ground.matrix.scale(32,0.0,32);
  ground.render();
}

function skyBox()
{
  var sky = new Cube();
  if (showNormal)
  {
    sky.textureNum = -3;
  }
  else
  {
    sky.textureNum = 0
  }
  sky.specularOn = 1;
  // sky.color = [1.0,0.2,0.3];
  sky.matrix.translate(32, -1.5, 32);
  sky.matrix.scale(32,32,32);
  sky.matrix.rotate(180,0,1,0);
  sky.render();

}

function drawMap()
{
  // the animal is looking at the left of the map
  
  for (var x = 0; x < map.length; x++)
  {
    for (var z=0; z < map.length; z++)
    {
      if (map[x][z] == 1)
      {
        let w = new Cube();
        w.textureNum = 2;
        w.matrix.translate(x, -0.8, z);
        wall.push(w)
      }
    }
  }
}

