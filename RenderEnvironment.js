function lightBox()
{
  lightX = Math.sin(g_seconds)*5 + 20
  gl.uniform3f(u_lightPos, lightX, lightCoord[1], lightCoord[2]); // pass light to shader
  gl.uniform3f(u_cameraPos, eye.elements[0], eye.elements[1], eye.elements[2]); // pass camera pos to shader
  var light = new Cube ();
  light.color = [2,2,0,1];
  light.textureNum = -2
  light.matrix.translate(lightX, lightCoord[1], lightCoord[2]);
  light.matrix.scale(-0.5,-0.5,-0.5);
  light.render();
}

